		<p class="text-justify">Logistics is an ever-expanding industry with seamless opportunities both locally and internationally. Candidates can look forward to challenging career opportunities in industry-specific domains or those that cut across many industries.</p>
									<p class="text-justify">In January 2012, the Academy of International Trade and Transport (AITT) the training arm of the Sri Lanka Logistics and Freight Forwarders&rsquo; Association (SLFFA) appointed Achievers Lanka Business School as their exclusive training partner to facilitate the conduct of the well renowned logistics and freight forwarding qualifications offered by AITT. Achievers Lanka Business School provides the training facilities and academic guidelines for AITT.</p>
									<p class="text-justify">The programmes offered are designed to equip students with effective technical and managerial skills necessary to become efficient logisticians at the supervisory and managerial levels. In addition, the programmes aim to provide the essential knowledge and pre-requisites for the pursuit of further education in business or logistics management.</p>
									<p class="text-justify">The field of logistics and supply chain management is diverse. The future of the industry is bright in ever expanding globalization of manufacturing and sourcing of goods and technologies, that will be taking place due to ever demanding costs of products by the markets. Sri Lanka is well position to plug into the growth in global and regional trade for the Indian sub-continent region, being next door to India one of the largest manufacturing economies and consumer market. The multi-disciplinary nature of Logistics and Supply Chain Management programmes today offered by universities, institutes and academies provides management graduates with an edge to compete successfully in many industries.</p>
		<div id="read_more" class="collapse">	
									<p class="text-justify">Candidates can look forward to challenging career opportunities in industry-specific domains or those that cut across many industries. These can fit many job descriptions such as logistics/transport/shipping manager, inventory control and materialist specialist, supply chain manager, warehousing manager, port/airport manager, trade pricing specialist, import/export manager, procurement manager, channel sales manager and customer logistics analyst.</p>
									<p class="text-justify">There are two paths-ways, one can get an entry to this industry, by following an academic path (Graduate programme in Logistics and Supply Chain Management, MBA in International Trade and Logistics) and join the industry and which still needs to undergo an apprentice period to learn the skills which is highly required to perform in real life situations to overcome the challenges and solutions that are demanded by the market.</p>
									<p class="text-justify">The other is to join the industry and undergo an apprenticeship period of leaning the skills to perform services on an international environment and take-up professional courses as you start to gain practical exposure, like learning foundation aspects of the industry, followed by a certificate and diploma and higher diploma courses which will enhance your knowledge easily as you have already been exposed to the required level of skills on the practical side, the 2nd option one can say would in actual fact develop a much faster level of acquiring professional standards in one self with this combination of experience and skill to be further enhanced with an academic study path.</p>
									<p class="text-justify">Both learning approaches can be considered fruitful and constructive pathways for new comers to this industry.</p>
									<p class="text-justify">Today in the post war- era of Sri Lanka, with the building of several ports and airports infrastructure, the logistics industry will have several opportunities for employment of professionals which the country needs to have in-order to successfully managed these investments for the nation.</p>
