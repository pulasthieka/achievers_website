			<p class="text-justify">		
								ACCA is a professional accountancy qualification recognized in more than 170 countries. On your pathway to completing ACCA you will gain a number of different qualifications, including a Bsc.(Hons) Degree from the prestigious Oxford Brookes University in the UK. ACCA is a global qualification preferred by employers, educational institutes, and governments all over the world. The competitive job market constantly requires competent finance professionals, consequently increasing the worldwide demand for ACCA qualified individuals. The ACCA professional qualification enables you to become a Chartered Certified Accountant and use the designation &lsquo;ACCA&rsquo; after your name. It prepares you to work in the fields of audit, taxation, financial accounting, management accounting and various other diversified areas.</p>
									<p class="text-justify">Achievers provides you with comprehensive courses on all three levels of the ACCA qualification under its panel of world-class lecturers leading you to excellence.</p>
									<p class="text-justify">Founded in 1930, the Association of Chartered Certified Accountants (ACCA) is the global professional accounting body offering the Chartered Certified Accountant qualification (ACCA or FCCA). ACCA has 178,000 members and 455,000 students in 180 countries.</p>
									<p class="text-justify">Chartered Certified Accountant is the professional body&rsquo;s main qualification. Following completion of up to 14 professional examinations, three years of supervised, relevant accountancy experience and a professional ethics module, it enables an individual to become a Chartered Certified Accountant.</p>
									<p class="text-justify">The ACCA professional examinations are offered worldwide four times yearly, in March, June, September and December as paper-based exams. A Bachelor of Science (Honors) degree in Applied Accounting (after completing the Fundamentals level of the exams, the professional ethics module and submitting a Research and Analysis Project), is offered in association with Oxford Brookes University.</p>
									<p class="text-justify">The syllabus comprises 14 examinations, although some exemptions are available. The qualification is structured in two parts.</p>
								
